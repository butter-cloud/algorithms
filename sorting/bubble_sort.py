def bubble_sort(arr):
    n = len(arr)
    for i in range(n):
        # ì´ë¯¸ ì •ë ¬ëœ ìƒíƒœì¸ì§€ í™•ì¸í•˜ëŠ” í”Œë˜ê·¸
        swapped = False
        # ë§ˆì§€ë§‰ iê°œëŠ” ì´ë¯¸ ì •ë ¬ë˜ì–´ ìˆìœ¼ë¯€ë¡œ ì œì™¸. (ê°€ì¥ í° ê°’ì„ ë’¤ë¡œ ë³´ëƒˆìŒ)
        for j in range(n - 1 - i):
            if arr[j] > arr[j + 1]:
                # ê°’ êµí™˜
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True
        # í•œ ë²ˆë„ êµí™˜ì´ ì¼ì–´ë‚˜ì§€ ì•Šì•˜ë‹¤ë©´ ì´ë¯¸ ì •ë ¬ëœ ìƒíƒœ
        if not swapped:
            break
    return arr


